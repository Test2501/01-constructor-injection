
public class LearnAnnotations {

    /**
     * @SpringBootApplication: This annotation is used to mark the main class of a Spring Boot application.
     * It combines three annotations - @Configuration, @EnableAutoConfiguration, and @ComponentScan.
     *
     * @RestController: This annotation is used to mark a class as a RESTful controller.
     * It's used to handle incoming HTTP requests and send back a response.
     *
     * @RequestMapping: This annotation is used to map a URL request to a controller method.
     * It's used to specify the HTTP method (GET, POST, etc.) and the URL pattern.
     *
     * @Autowired: This annotation is used to inject a dependency into a Spring bean.
     * It's used to declare a dependency on another bean or a value that Spring Boot should provide.
     *
     * @Component: This annotation is used to mark a class as a Spring bean.
     * It's used to tell Spring Boot that this class should be managed by the Spring container.
     *
     * @Configuration: This annotation is used to mark a class as a Spring configuration class.
     * It's used to specify the beans and their dependencies.
     *
     * @EnableAutoConfiguration: This annotation is used to enable Spring Boot's auto-configuration feature.
     * It's used to let Spring Boot configure the application based on the classpath and other settings.
     *
     * @Value: This annotation is used to inject a value into a Spring bean. It's used to provide a value that's external to the application code.
     *
     * @ConditionalOnProperty: This annotation is used to conditionally enable a Spring bean based on the value of a property.
     * It's used to enable or disable a bean based on configuration settings.
     */

}
